package pharmacy.drug_admin_system.enums;

public enum ConcentrationUnit {
    MG_ML,       // Milligrams per milliliter
    MCG_ML,      // Micrograms per milliliter
    MG_G,        // Milligrams per gram
    IU_ML,       // International Units per milliliter
    MG_TAB,      // Milligrams per tablet
    MG_CAP,      // Milligrams per capsule
    PERCENT,     // Percent (e.g., 1%)
    MEQ_ML,      // Milli-equivalents per milliliter
    MG_PATCH,    // Milligrams per patch
    MG_SPRAY,    // Milligrams per spray
    MG_HOUR,     // Milligrams per hour (infusion)
    G_L          // Grams per liter
}
